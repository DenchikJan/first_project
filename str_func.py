def upper_str(value: str):
    """
    Преобразует все буквы в верхний регистр
    :param value: str
    :return: str
    """
    return value.upper()


def title_str(value: str):
    """
    Делает заглавными первые буквы каждого слова в строке
    :param value: str
    :return: str
    """
    return value.title()
